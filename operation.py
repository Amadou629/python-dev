x = int(input("Entrer un entier : "))
y = float(input("entrer un nombre : "))
# Addition
sum = x + y
print(f"la somme de {x} et {y} est : {sum}\n")
# Difference
diff = x-y
print(f"la difference de {x} et {y} est : {diff}\n")
# Multiplication
prod = x*y
print(f"le produit de {x} et {y} est : {prod}\n")
# Division
div = x/y
print(f"la division de {x} et {y} est : {div}\n")
# Modulo
mod = x % y
print(f"le modulo de {x} et {y} est : {mod}")