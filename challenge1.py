if o == '+':
    print(f"la somme de {x} et {y} est : {x + y}\n")
# SOUSTRACTION
elif o == '-':
    print(f"la difference de {x} et {y} est {x - y}\n")
# MULTIPLICATION
elif o == '*':
    print(f"la multiplication de {x} et {y} est {x * y}\n")
